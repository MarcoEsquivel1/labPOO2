    private void limpiar(){
        this.jComboBox1.setSelectedIndex(-1);
        this.txtNombre.setText("");
        this.jFormattedTextField1.setValue(0);
        this.spnOficinas.setValue(1);
        this.spnPlantas.setValue(1);
    }
    
    private void validar(){
        this.spnOficinas.setEnabled(false);
        this.spnPlantas.setEnabled(false);
        switch(this.jComboBox1.getSelectedIndex()){
            case 0:
                this.spnOficinas.setEnabled(false);
                this.spnPlantas.setEnabled(false);               
                break;
            case 1:
                this.spnOficinas.setEnabled(true);
                this.spnPlantas.setEnabled(true);
                break;
            case 2:
                this.spnOficinas.setEnabled(true);
                this.spnPlantas.setEnabled(true);
                break;
            default:
                this.spnOficinas.setEnabled(false);
                this.spnPlantas.setEnabled(false);   
                break;
        }
    }
    private String tipo(int x){
        String t = "";
        switch(x){
            case 0:
                t = "Polideportivo";
                break;
            case 1:
                t = "Educacional";
                break;
            case 2:
                t = "Hospitalario";
                break;
        } 
        return t;
    }
