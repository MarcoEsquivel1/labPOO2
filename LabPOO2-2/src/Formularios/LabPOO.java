    private void btnConstruirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConstruirActionPerformed

        if (!"".equals(this.txtNombre.getText()) && Double.parseDouble(this.jFormattedTextField1.getValue().toString()) > 0) {
            switch(this.jComboBox1.getSelectedIndex()){
                case 0:
                    Construccion polideportivo = new Construccion(this.txtNombre.getText(), this.jComboBox1.getSelectedIndex(), Double.parseDouble(this.jFormattedTextField1.getValue().toString()));              
                    this.jLabel8.setIcon(new ImageIcon("Polidep.png"));
                    this.lblSeHa.setText("¡Se ha construido un POLIDEPORTIVO.!");
                    this.lblNom.setText("Nombre: "+ polideportivo.getNombre());
                    this.lblTip.setText("Tipo: "+tipo(polideportivo.getTipoEd()));
                    this.lblSup.setText("Superficie: "+ polideportivo.getSuperficieEd()+ " m^2");
                    break;
                case 1:
                    ConstruccionConOficinas educacional = new ConstruccionConOficinas(this.txtNombre.getText(), this.jComboBox1.getSelectedIndex(), Double.parseDouble(this.jFormattedTextField1.getValue().toString()), Integer.parseInt(this.spnOficinas.getValue().toString()), Integer.parseInt(this.spnPlantas.getValue().toString()));
                    this.jLabel8.setIcon(new ImageIcon("Educ.jpg"));
                    this.lblSeHa.setText("¡Se ha construido un Edificio Educacional.!");
                    this.lblNom.setText("Nombre: "+ educacional.getNombre());
                    this.lblTip.setText("Tipo: "+tipo(educacional.getTipoEd()));
                    this.lblSup.setText("Superficie: "+ educacional.getSuperficieEd()+ " m^2");
                    this.lblOf.setText("Oficinas: "+ educacional.getCantOficinas());
                    this.lblPlan.setText("Plantas: " + educacional.getCantPlantas());
                    break;
                case 2:
                    ConstruccionConOficinas hospitalario = new ConstruccionConOficinas(this.txtNombre.getText(), this.jComboBox1.getSelectedIndex(), Double.parseDouble(this.jFormattedTextField1.getValue().toString()), Integer.parseInt(this.spnOficinas.getValue().toString()), Integer.parseInt(this.spnPlantas.getValue().toString()));
                    this.jLabel8.setIcon(new ImageIcon("hosp.png"));
                    this.lblSeHa.setText("¡Se ha construido un Edificio Hospitalario.!");
                    this.lblNom.setText("Nombre: "+ hospitalario.getNombre());
                    this.lblTip.setText("Tipo: "+tipo(hospitalario.getTipoEd()));
                    this.lblSup.setText("Superficie: "+ hospitalario.getSuperficieEd()+ " m^2");
                    this.lblOf.setText("Oficinas: "+ hospitalario.getCantOficinas());
                    this.lblPlan.setText("Plantas: " + hospitalario.getCantPlantas());
                    break;
                default:
                    break;
            }        
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese los valores requeridos");
        }
        limpiar();
    }//GEN-LAST:event_btnConstruirActionPerformed

